/**3. Convertir grados celcius a Fahrenheit */

//se declara la variable celsius inicializada
let celcius = 30

//se declara otra variable llamada fahrenheit que va a almacenar el resultado de la operación de la Fórmula para convertir Grados a Fahrenheit
let fahrenheit = (celcius * 9) / 5 + 32
console.log(fahrenheit, '°F' )